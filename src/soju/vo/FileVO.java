package soju.vo;

public class FileVO {
	private int files_num;
	private String files_type;
	private String files_path;
	private int bd_num;
	
	public int getFiles_num() {
		return files_num;
	}
	public void setFiles_num(int files_num) {
		this.files_num = files_num;
	}
	public String getFiles_type() {
		return files_type;
	}
	public void setFiles_type(String files_type) {
		this.files_type = files_type;
	}
	public String getFiles_path() {
		return files_path;
	}
	public void setFiles_path(String files_path) {
		this.files_path = files_path;
	}
	public int getBd_num() {
		return bd_num;
	}
	public void setBd_num(int bd_num) {
		this.bd_num = bd_num;
	}
}
