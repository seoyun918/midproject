package soju.vo;

public class NoticeVO {
	private int ntc_num;
	private String ntc_title;
	private String ntc_cont;
	private String ntc_date;
	private int mng_num;
	
	public int getNtc_num() {
		return ntc_num;
	}
	public void setNtc_num(int ntc_num) {
		this.ntc_num = ntc_num;
	}
	public String getNtc_title() {
		return ntc_title;
	}
	public void setNtc_title(String ntc_title) {
		this.ntc_title = ntc_title;
	}
	public String getNtc_cont() {
		return ntc_cont;
	}
	public void setNtc_cont(String ntc_cont) {
		this.ntc_cont = ntc_cont;
	}
	public String getNtc_date() {
		return ntc_date;
	}
	public void setNtc_date(String ntc_date) {
		this.ntc_date = ntc_date;
	}
	public int getMng_num() {
		return mng_num;
	}
	public void setMng_num(int mng_num) {
		this.mng_num = mng_num;
	}
}
