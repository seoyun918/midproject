package soju.vo;

public class CalendarVO {
	private int cal_num;
	private String cal_title;
	private String cal_cont;
	private String cal_start;
	private String cal_end;
	private int allday;
	private String event_lgu;
	private int art_num;
	
	public int getCal_num() {
		return cal_num;
	}
	public void setCal_num(int cal_num) {
		this.cal_num = cal_num;
	}
	public String getCal_title() {
		return cal_title;
	}
	public void setCal_title(String cal_title) {
		this.cal_title = cal_title;
	}
	public String getCal_cont() {
		return cal_cont;
	}
	public void setCal_cont(String cal_cont) {
		this.cal_cont = cal_cont;
	}
	public String getCal_start() {
		return cal_start;
	}
	public void setCal_start(String cal_start) {
		this.cal_start = cal_start;
	}
	public String getCal_end() {
		return cal_end;
	}
	public void setCal_end(String cal_end) {
		this.cal_end = cal_end;
	}
	public int getAllday() {
		return allday;
	}
	public void setAllday(int allday) {
		this.allday = allday;
	}
	public String getEvent_lgu() {
		return event_lgu;
	}
	public void setEvent_lgu(String event_lgu) {
		this.event_lgu = event_lgu;
	}
	public int getArt_num() {
		return art_num;
	}
	public void setArt_num(int art_num) {
		this.art_num = art_num;
	}
	@Override
	public String toString() {
		return "CalendarVO [cal_num=" + cal_num + ", cal_title=" + cal_title + ", cal_cont=" + cal_cont + ", cal_start="
				+ cal_start + ", cal_end=" + cal_end + ", allday=" + allday + ", event_lgu=" + event_lgu + ", art_num="
				+ art_num + "]";
	}
	
	
}
