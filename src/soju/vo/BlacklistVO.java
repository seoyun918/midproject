package soju.vo;

public class BlacklistVO {
	private int blk_num;
	private String blk_sdate;
	private String blk_edate;
	private String blk_reason;
	private int mem_num;
	
	public int getBlk_num() {
		return blk_num;
	}
	public void setBlk_num(int blk_num) {
		this.blk_num = blk_num;
	}
	public String getBlk_sdate() {
		return blk_sdate;
	}
	public void setBlk_sdate(String blk_sdate) {
		this.blk_sdate = blk_sdate;
	}
	public String getBlk_edate() {
		return blk_edate;
	}
	public void setBlk_edate(String blk_edate) {
		this.blk_edate = blk_edate;
	}
	public String getBlk_reason() {
		return blk_reason;
	}
	public void setBlk_reason(String blk_reason) {
		this.blk_reason = blk_reason;
	}
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
}
